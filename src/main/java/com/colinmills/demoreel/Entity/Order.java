package com.colinmills.demoreel.Entity;

public class Order {
    private float qty; // quantity
    private float  prc; // price

    public float getQty() {
        return qty;
    }

    public void setQty(float qty) {
        this.qty = qty;
    }

    public float getPrc() {
        return prc;
    }

    public void setPrc(float prc) {
        this.prc = prc;
    }
}
